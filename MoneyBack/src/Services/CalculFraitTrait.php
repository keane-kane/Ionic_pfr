<?php

namespace App\Services;

trait CalculFraitTrait {

    public $com = array(
        425   => [0, 5000],
        850   => [5000, 10000],
        1270  => [10000, 15000],
        1695  => [15000, 20000],
        2500  => [20000, 50000],
        3000  => [50000, 60000],
        4000  => [60000, 75000],
        5000  => [75000, 120000],
        6000  => [120000, 150000],
        7000  => [150000, 200000],
        8000  => [200000, 250000],
        9000  => [250000, 300000],
        12000 => [300000, 400000],
        15000 => [400000, 750000],
        22000 => [750000, 900000],
        25000 => [900000, 1000000],
        27000 => [1000000, 1125000],
        30000 => [1125000, 1400000],
        35000 => [1400000, 2000000],
    );

    public function getFrait($m) {
        $i = 0;
          if($m >= 2000000){
            return  $m * 0.02;
          }else {
            foreach ($this->com as $key => $value) {
              if($m >= $value[$i] && ($m+1) <= ($value[$i+1]))
                return $key;
            }
          }

    }


}